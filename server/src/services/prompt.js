export function addNotePrefix(text) {
  const prefix = `假設您是一位專業講師，請依據以下文字內容，幫我撰寫一份適合放入 Notion 教學模板的教學講義。內容主題來自 [txt字幕輸出後] 的內容，請依格式完成：

1. 開場導語
   - 用簡單易懂的語言說明主題的重要性與學習價值。

2. 教學重點
   - 條列您在內容中看見的所有核心知識點，並為每個知識點撰寫 2-3 句的簡短說明，與白話解釋，並且提供一個舉例與回答。

3. 一個實作任務
   - 提供逐步指引，讓學員能夠親自應用所學。
   - 描述兩個真實應用情境，幫助學員理解學以致用。

4. 結語與思考題
   - 撰寫簡短的收尾語，鼓勵學員持續學習。
   - 提供 1-2 個思考題，讓學員反思並能與日常生活/工作連結。
`;
  return `${prefix}

${text}`.trim();
}
